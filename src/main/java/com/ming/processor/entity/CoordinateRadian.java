package com.ming.processor.entity;

public class CoordinateRadian {

    private double x;

    private double y;

    public CoordinateRadian(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
